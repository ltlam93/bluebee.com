	// Export data in HTML/CSV/Word/Excel/Email/PDF format
	function ExportDocument(&$Doc, &$Recordset, $StartRec, $StopRec, $ExportPageType = "") {
		if (!$Recordset || !$Doc)
			return;

		// Write header
		$Doc->ExportTableHeader();
		if ($Doc->Horizontal) { // Horizontal format, write header
			$Doc->BeginExportRow();
			if ($ExportPageType == "view") {
				if ($this->news_id->Exportable) $Doc->ExportCaption($this->news_id);
				if ($this->news_university->Exportable) $Doc->ExportCaption($this->news_university);
				if ($this->news_title->Exportable) $Doc->ExportCaption($this->news_title);
				if ($this->news_content->Exportable) $Doc->ExportCaption($this->news_content);
				if ($this->news_active->Exportable) $Doc->ExportCaption($this->news_active);
			} else {
				if ($this->news_id->Exportable) $Doc->ExportCaption($this->news_id);
				if ($this->news_university->Exportable) $Doc->ExportCaption($this->news_university);
				if ($this->news_title->Exportable) $Doc->ExportCaption($this->news_title);
				if ($this->news_active->Exportable) $Doc->ExportCaption($this->news_active);
			}
			$Doc->EndExportRow();
		}

		// Move to first record
		$RecCnt = $StartRec - 1;
		if (!$Recordset->EOF) {
			$Recordset->MoveFirst();
			if ($StartRec > 1)
				$Recordset->Move($StartRec - 1);
		}
		while (!$Recordset->EOF && $RecCnt < $StopRec) {
			$RecCnt++;
			if (intval($RecCnt) >= intval($StartRec)) {
				$RowCnt = intval($RecCnt) - intval($StartRec) + 1;

				// Page break
				if ($this->ExportPageBreakCount > 0) {
					if ($RowCnt > 1 && ($RowCnt - 1) % $this->ExportPageBreakCount == 0)
						$Doc->ExportPageBreak();
				}
				$this->LoadListRowValues($Recordset);

				// Render row
				$this->RowType = EW_ROWTYPE_VIEW; // Render view
				$this->ResetAttrs();
				$this->RenderListRow();
				$Doc->BeginExportRow($RowCnt); // Allow CSS styles if enabled
				if ($ExportPageType == "view") {
					if ($this->news_id->Exportable) $Doc->ExportField($this->news_id);
					if ($this->news_university->Exportable) $Doc->ExportField($this->news_university);
					if ($this->news_title->Exportable) $Doc->ExportField($this->news_title);
					if ($this->news_content->Exportable) $Doc->ExportField($this->news_content);
					if ($this->news_active->Exportable) $Doc->ExportField($this->news_active);
				} else {
					if ($this->news_id->Exportable) $Doc->ExportField($this->news_id);
					if ($this->news_university->Exportable) $Doc->ExportField($this->news_university);
					if ($this->news_title->Exportable) $Doc->ExportField($this->news_title);
					if ($this->news_active->Exportable) $Doc->ExportField($this->news_active);
				}
				$Doc->EndExportRow();
			}
			$Recordset->MoveNext();
		}
		$Doc->ExportTableFooter();
	}

